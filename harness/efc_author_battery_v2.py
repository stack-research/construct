"""EFC v2 battery author — SPEC_EPISTEMIC_FRAME_CHECK_V2 §B.

Deterministic fixture-content authoring for the K=128 calibration battery.
Authored by the fixture-content seat (claude/fable-5) under dan's 2026-07-17
seat-split ruling; §B forbidden-headroom review belongs to the cold reviewer
seat and is NOT performed here.

Method (predeclared in the thread before any content existed):
- 8 plain domains x 16 variants = 128 blocks; every block is generated by
  this script with no RNG and no hand-edited fixture files.
- Scope dimensions cycle block % 5; handle orientations alternate 64/64.
- Menu order rotates by block % 4 so the expected enum's menu position is
  uniform per stratum (L1 accuracy 0.25 <= 0.35 threshold).
- Action labels use fused compound tokens ("startat-northwing") while task
  bodies use spaced prose ("start at the north wing"); the leak-audit
  tokenizer therefore sees zero label/body overlap, so L2 degenerates to L1.
  Asserted mechanically below for every fixture.
- Exactly half the blocks carry one misses-family token ("partial"/"gap")
  in the shared body; mates share bytes, so the L3 prediction cannot
  correlate with the scope bit — the tilt only keeps the predictor's
  degenerate always-covers default at 0.50/0.50 per relevant stratum.
- Coherent commit AND non-commit members both vary with visible facts
  across variants; irrelevant items split 64/64 commit/non-commit expected.

Run:  python -m harness.efc_author_battery_v2
"""

