def _rotate(seq: list[str], r: int) -> list[str]:
    r %= len(seq)
    return seq[r:] + seq[:r]


def _decision_scope(block_index: int, domain_index: int, variant: int) -> str:
    return format_scope_dimensions({
        "population": f"group-{block_index:03d}",
        "interval": "2026-q4",
        "jurisdiction": f"area-{domain_index}",
        "endpoint": f"desk-{domain_index}.local/v1",
        "artifact_version": f"{(variant % 3) + 1}.0",
    })


def _trigger_fields(
    *,
    relevant: bool,
    source_reference: str,
    decision_scope: str,
) -> dict[str, object]:
    return {
        "assertion_basis_kind": "cited_source" if relevant else "direct_observation",
        "observation_boundary_present": not relevant,
        "source_reference_present": relevant,
        "decision_scope_present": True,
        "source_reference": source_reference,
        "decision_scope": decision_scope,
    }


def _relevant_body(
    domain: DomainSpec,
    *,
    tid: str,
    commit_choice: int,
    noncommit_choice: int,
    tilt_token: str | None,
) -> str:
    ent = domain.entities[commit_choice]
    other = domain.entities[1 - commit_choice]
    return " ".join((
        domain.request_line.format(tid=tid, ent=ent),
        CITED_SENTENCE,
        POLICY_SENTENCE,
        domain.commit_fact.format(ent=ent, other=other),
        domain.noncommit_facts[noncommit_choice],
        FILING_SENTENCES[tilt_token],
    ))


def build_block(block_index: int) -> list[dict[str, object]]:
    domain_index = block_index // VARIANTS_PER_DOMAIN
    variant = block_index % VARIANTS_PER_DOMAIN
    domain = DOMAINS[domain_index]

    commit_choice = variant % 2
    noncommit_choice = (variant // 2) % 2
    irrelevant_commit_role = ((variant // 4) % 2) == 0
    irrelevant_sub = (variant // 8) % 2

    block_id = f"cal-b{block_index:03d}"
    tid = f"T-{block_index:03d}"
    oid = f"O-{block_index:03d}"
    dim = SCOPE_DIMENSIONS[block_index % len(SCOPE_DIMENSIONS)]
    orientation = "A" if block_index % 2 == 0 else "B"
    tilted = ((block_index // 2) % 2) == 0
    tilt_token = TILT_TOKENS[(block_index // 4) % 2] if tilted else None
    rotation = block_index % 4

    labels = list(domain.commit_labels) + list(domain.noncommit_labels)
    role_map = {
        domain.commit_labels[0]: "commit",
        domain.commit_labels[1]: "commit",
        domain.noncommit_labels[0]: "non_commit",
        domain.noncommit_labels[1]: "non_commit",
    }
    coherent_commit = domain.commit_labels[commit_choice]
    other_commit = domain.commit_labels[1 - commit_choice]
    coherent_noncommit = domain.noncommit_labels[noncommit_choice]
    other_noncommit = domain.noncommit_labels[1 - noncommit_choice]
    menu_order = _rotate(
        [coherent_commit, coherent_noncommit, other_commit, other_noncommit],
        rotation,
    )

    task_body = _relevant_body(
        domain,
        tid=tid,
        commit_choice=commit_choice,
        noncommit_choice=noncommit_choice,
        tilt_token=tilt_token,
    )
    decision_scope = _decision_scope(block_index, domain_index, variant)

    shared: dict[str, object] = {
        "block_id": block_id,
        "population_id": POPULATION_ID,
        "coherent_commit_action": coherent_commit,
        "coherent_non_commit_action": coherent_noncommit,
        "missing_scope_dimension": dim,
        "handle_orientation": orientation,
        "action_set": list(labels),
        "menu_order": list(menu_order),
        "role_map": dict(role_map),
        "task_body": task_body,
    }

    fixtures: list[dict[str, object]] = []
    for stratum, scope_bit in (("match", "covers"), ("mismatch", "misses")):
        mate_orientation = (
            orientation
            if stratum == "match"
            else complementary_handle_orientation(orientation)
        )
        handle = expected_opaque_source_handle(block_id, mate_orientation)
        fixture_id = f"{block_id}-{stratum}"
        fixtures.append({
            **shared,
            "fixture_id": fixture_id,
            "task_id": fixture_id,
            "stratum": stratum,
            "scope_bit": scope_bit,
            "opaque_source_handle": handle,
            "expected_commitment_enum": (
                coherent_commit if scope_bit == "covers" else coherent_noncommit
            ),
            **_trigger_fields(
                relevant=True,
                source_reference=expected_source_reference(handle),
                decision_scope=decision_scope,
            ),
        })

    if irrelevant_commit_role:
        expected = domain.commit_labels[commit_choice]
        template = domain.irrelevant_commit[irrelevant_sub]
        ir_entity = domain.entities[commit_choice]
    else:
        expected = domain.noncommit_labels[irrelevant_sub]
        template = domain.irrelevant_noncommit[irrelevant_sub]
        ir_entity = domain.entities[commit_choice]
    remaining = [label for label in labels if label != expected]
    ir_menu = _rotate([expected, *remaining], rotation)
    fixture_id = f"{block_id}-irrelevant"
    fixtures.append({
        "fixture_id": fixture_id,
        "block_id": block_id,
        "task_id": fixture_id,
        "population_id": POPULATION_ID,
        "stratum": "irrelevant",
        "action_set": list(labels),
        "menu_order": ir_menu,
        "role_map": dict(role_map),
        "expected_commitment_enum": expected,
        "task_body": template.format(oid=oid, ent=ir_entity),
        **_trigger_fields(
            relevant=False,
            source_reference="",
            decision_scope=decision_scope,
        ),
    })
    return fixtures


def build_suite() -> list[dict[str, object]]:
    fixtures: list[dict[str, object]] = []
    for block_index in range(K_PAIRS):
        fixtures.extend(build_block(block_index))
    return fixtures


def assert_authoring_invariants(fixtures: list[dict[str, object]]) -> None:
    """Author-time mechanical invariants beyond the built validators."""
    bodies: set[str] = set()
    for fixture in fixtures:
        body = fixture["task_body"]
        assert isinstance(body, str)
        body_tokens = set(tokens(body))
        action_set = fixture["action_set"]
        assert isinstance(action_set, list)
        for label in action_set:
            assert isinstance(label, str)
            overlap = set(tokens(label)) & body_tokens
            assert not overlap, (
                f"label/body token overlap in {fixture['fixture_id']}: {overlap}"
            )
        markers = body_tokens & L3_MARKER_TOKENS
        if fixture["stratum"] == "irrelevant":
            assert not markers, (
                f"marker token in irrelevant body {fixture['fixture_id']}: {markers}"
            )
        else:
            assert markers in ({"partial"}, {"gap"}, set()), (
                f"unexpected marker set in {fixture['fixture_id']}: {markers}"
            )
        bodies.add(body)
    # 128 shared relevant bodies + 128 irrelevant bodies, all distinct.
    assert len(bodies) == 2 * K_PAIRS, f"non-distinct bodies: {len(bodies)}"

    tilted = sum(
        1
        for fixture in fixtures
        if fixture["stratum"] == "match"
        and set(tokens(fixture["task_body"])) & L3_MARKER_TOKENS  # type: ignore[arg-type]
    )
    assert tilted == K_PAIRS // 2, f"tilt imbalance: {tilted}"

    irrelevant_commit = sum(
        1
        for fixture in fixtures
        if fixture["stratum"] == "irrelevant"
        and fixture["role_map"][fixture["expected_commitment_enum"]] == "commit"  # type: ignore[index]
    )
    assert irrelevant_commit == K_PAIRS // 2, (
        f"irrelevant commit/non-commit imbalance: {irrelevant_commit}"
    )


def write_suite(fixtures: list[dict[str, object]]) -> str:
    FIXTURES_DIR.mkdir(parents=True, exist_ok=True)
    for fixture in fixtures:
        path = FIXTURES_DIR / f"{fixture['fixture_id']}.json"
        path.write_text(
            json.dumps(fixture, sort_keys=True, indent=2, ensure_ascii=False)
            + "\n",
            encoding="utf-8",
        )
    suite_manifest = {
        "suite_id": POPULATION_ID,
        "fixtures": [{"fixture_id": fx["fixture_id"]} for fx in fixtures],
    }
    (SUITE_DIR / "suite_manifest.json").write_text(
        json.dumps(suite_manifest, indent=2) + "\n", encoding="utf-8"
    )
    store = build_record_store_from_fixtures(fixtures)
    (SUITE_DIR / "provenance_record_store.json").write_text(
        json.dumps(record_store_canonical_payload(store), indent=2) + "\n",
        encoding="utf-8",
    )
    return suite_hash(fixtures, k_pairs=K_PAIRS, record_store=store)


def main() -> None:
    fixtures = build_suite()
    assert_authoring_invariants(fixtures)
    store = build_record_store_from_fixtures(fixtures)
    result = validate_suite(fixtures, record_store=store)
    if not result.ok:
        raise SystemExit(f"validate_suite refused: {result.refusals[:5]}")
    leak = evaluate_leak_audit(fixtures)
    if not leak.ok:
        raise SystemExit(f"leak audit refused: {leak.refusals}")
    digest = write_suite(fixtures)
    print(f"fixtures: {result.fixture_count}")
    for cell in leak.cells:
        print(
            f"leak {cell.leg}/{cell.stratum}: {cell.correct}/{cell.total} "
            f"acc={cell.accuracy:.4f} thr={cell.fail_threshold:.2f}"
        )
    print(f"fixture_suite_hash: {digest}")


if __name__ == "__main__":
    main()
